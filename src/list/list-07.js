// ====================== pair ========================
// 接收两个参数，fst 和 snd，返回数组 [fst, snd]
R.pair('foo', 'bar'); //=> ['foo', 'bar']

// ====================== fromPairs =====================
// 由一系列 “键值对” 创建一个 object。如果某个键出现多次，选取最右侧的键值对
R.fromPairs([['a', 1], ['b', 2], ['c', 3]]); //=> {a: 1, b: 2, c: 3}
